// Fonction helper pour obtenir la traduction
export const t = (key, langue = 'fr') => {
  const keys = key.split('.');
  let value = translations[langue] || translations['fr'];
  for (let k of keys) {
    value = value[k];
    if (!value) return key; // Retourner la clé si la traduction n'existe pas
  }
  return value;
};
